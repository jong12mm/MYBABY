package Ch38_JUNIT;

public class C00JUNITMethodINFO {

//	@Test: 이 어노테이션은 테스트 메서드임을 나타내는 데 사용됩니다. 
//	JUnit이 해당 메서드를 테스트 케이스로 인식하고 실행합니다.
//
//	@Before: 이 어노테이션은 각 테스트 메서드가 실행되기 전에 실행되어야 
//	하는 초기화 코드를 포함하는 메서드임을 나타냅니다.
//
//	@After: 이 어노테이션은 각 테스트 메서드가 실행된 후에 실행되어야 하는 
//	정리 코드를 포함하는 메서드임을 나타냅니다.
//
//	@BeforeClass: 이 어노테이션은 모든 테스트 메서드보다 먼저 실행되어야 
//	하는 초기화 코드를 포함하는 메서드임을 나타냅니다.
//
//	@AfterClass: 이 어노테이션은 모든 테스트 메서드가 실행된 후에 실행되어야 
//	하는 정리 코드를 포함하는 메서드임을 나타냅니다.
	
//	assertEquals(expected, actual): 이 메서드는 예상값(expected)과 실제값(actual)이 
//	같은지 비교합니다. 값이 다를 경우 테스트가 실패로 표시됩니다.
//
//	assertTrue(condition): 
//	이 메서드는 조건(condition)이 참인지 확인합니다. 조건이 거짓일 경우 테스트가 실패로 표시됩니다.
//
//	assertFalse(condition): 이 메서드는 조건(condition)이 거짓인지 확인합니다.
//	조건이 참일 경우 테스트가 실패로 표시됩니다.
//
//	assertNull(object): 이 메서드는 객체(object)가 null인지 확인합니다. 객체가 null이 아닐 경우 테스트가 실패로 표시됩니다.
//
//	assertNotNull(object): 이 메서드는 객체(object)가 null이 아닌지 확인합니다. 객체가 null일 경우 테스트가 실패로 표시됩니다.
//
//	assertSame(expected, actual): 이 메서드는 예상값(expected)과 실제값(actual)이 같은 객체인지 확인합니다. 객체가 다를 경우 테스트가 실패로 표시됩니다.
//
//	assertNotSame(expected, actual): 이 메서드는 예상값(expected)과 실제값(actual)이 다른 객체인지 확인합니다. 객체가 같을 경우 테스트가 실패로 표시됩니다.
//

}
